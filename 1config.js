// Funci√≥n para obtener la URL de una imagen
function obtenerUrlImagen(tipo, id) {
    if (configImagenes[tipo] && configImagenes[tipo][id]) {
        return configImagenes[tipo][id];
    }
    
    // Si no encuentra, buscar en niveles anidados
    for (const categoria in configImagenes) {
        if (typeof configImagenes[categoria] === 'object' && configImagenes[categoria][id]) {
            return configImagenes[categoria][id];
        }
    }
    
    // Imagen por defecto si no se encuentra
    return 'imagenes/default.jpg';
}

// Funci√≥n para obtener imagen de quintilliza
function obtenerImagenQuintilliza(hermanaId, tipo = 'principal') {
    if (configImagenes.quintillizas && 
        configImagenes.quintillizas[hermanaId] && 
        configImagenes.quintillizas[hermanaId][tipo]) {
        return configImagenes.quintillizas[hermanaId][tipo];
    }
    return configImagenes.quintillizas[hermanaId]?.principal || 'imagenes/quintillizas/default.jpg';
}

