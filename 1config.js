// Funci√≥n para obtener video √≠ntimo espec√≠fico
function obtenerVideoIntimo(hermanaId, momentoId) {
    if (videosIntimosQuintillizas[hermanaId]) {
        const momento = videosIntimosQuintillizas[hermanaId].find(m => m.id === momentoId);
        return momento ? momento.url : null;
    }
    return null;
}

