// Agregar nuevo mazo (para testing/expansiÃ³n)
window.agregarNuevoMazoVideoH = function(refMazo, nuevasPalabras) {
    if (vocabularioVideosH[refMazo]) {
        console.log(`âŒ El mazo ${refMazo} ya existe`);
        return false;
    }
    
    if (!Array.isArray(nuevasPalabras) || nuevasPalabras.length === 0) {
        console.log("âŒ Las palabras deben ser un array no vacÃ­o");
        return false;
    }
    
    // Validar estructura de palabras
    const palabraValida = nuevasPalabras.every(p => 
        p.japones && p.lectura && p.opciones && Array.isArray(p.opciones) && 
        typeof p.respuesta === 'number'
    );
    
    if (!palabraValida) {
        console.log("âŒ Estructura de palabras invÃ¡lida");
        return false;
    }
    
    vocabularioVideosH[refMazo] = nuevasPalabras;
    console.log(`âœ… Nuevo mazo agregado: ${refMazo} con ${nuevasPalabras.length} palabras`);
    return true;
};

// Obtener palabras aleatorias para prÃ¡ctica
window.obtenerPalabrasAleatoriasVideoH = function(cantidad = 10) {
    const todasLasPalabras = [];
    
    // Recolectar todas las palabras
    Object.values(vocabularioVideosH).forEach(mazo => {
        if (Array.isArray(mazo)) {
            todasLasPalabras.push(...mazo);
        }
    });
    
    if (todasLasPalabras.length === 0) {
        console.log("âŒ No hay palabras disponibles");
        return [];
    }
    
    // Mezclar y seleccionar
    const mezcladas = [...todasLasPalabras];
    for (let i = mezcladas.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [mezcladas[i], mezcladas[j]] = [mezcladas[j], mezcladas[i]];
    }
    
    return mezcladas.slice(0, Math.min(cantidad, mezcladas.length));
};

