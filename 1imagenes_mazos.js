window.agregarImagenMazo = function(subcontenedorId, mazoId, urlImagen) {
    if (!imagenesMazos[subcontenedorId]) {
        imagenesMazos[subcontenedorId] = {};
        console.log(`üìÅ Creado nuevo subcontenedor: ${subcontenedorId}`);
    }
    
    imagenesMazos[subcontenedorId][mazoId] = urlImagen;
    console.log(`‚úÖ Imagen agregada: ${subcontenedorId}/${mazoId} = ${urlImagen}`);
    return true;
};

