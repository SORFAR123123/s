// FUNCI√ìN PARA OBTENER IMAGEN DE MAZO CON FALLBACK INTELIGENTE
// ============================================================================

function obtenerImagenMazo(subcontenedorId, mazoId) {
    // 1. Verificar si existe imagen espec√≠fica
    if (imagenesMazos[subcontenedorId] && imagenesMazos[subcontenedorId][mazoId]) {
        return imagenesMazos[subcontenedorId][mazoId];
    }
    
    // 2. Si no, verificar si existe imagen general en config.js
    if (typeof configImagenes !== 'undefined' && 
        configImagenes.mazos && 
        configImagenes.mazos[mazoId]) {
        return configImagenes.mazos[mazoId];
    }
    
    // 3. Si no, usar imagen por defecto seg√∫n el tema
    if (subcontenedorId.includes('sub1_')) {
        return 'imagenes/mazos/default_summer1.jpg';
    } else if (subcontenedorId.includes('sub2_')) {
        return 'imagenes/mazos/default_last_summer.jpg';
    } else if (subcontenedorId.includes('sub3_')) {
        return 'imagenes/mazos/default_yamada.jpg';
    } else if (subcontenedorId.includes('sub4_') || subcontenedorId.includes('sub5_')) {
        return 'imagenes/mazos/default_general.jpg';
    } else if (parseInt(subcontenedorId.replace('sub', '').split('_')[0]) >= 6) {
        return 'imagenes/mazos/default_future.jpg';
    }
    
    // 4. √öltimo recurso: imagen por defecto general
    return 'imagenes/mazos/default.jpg';
}

